"""
white_salary/core/exceptions.py

自定义异常类体系。

为什么不直接用Python自带的Exception？
因为自定义异常能让我们精确知道是哪个模块出了什么问题：
  - WhiteSalaryError        → 项目内的所有异常都继承这个
  - LLMError               → LLM（大语言模型）相关的错误
  - ASRError               → 语音识别相关的错误
  - TTSError               → 语音合成相关的错误
  - ...

这样在处理错误时，可以分门别类地处理：
    try:
        await llm.chat(...)
    except LLMConnectionError:
        # LLM连接失败，换一个LLM试试
    except LLMRateLimitError:
        # API调用太频繁了，等一下再试
"""


# =============================================================================
# 基础异常
# =============================================================================

class WhiteSalaryError(Exception):
    """
    White Salary 项目所有自定义异常的基类。

    所有自定义异常都必须继承这个类。
    这样可以用 `except WhiteSalaryError` 一次性捕获项目内的所有异常。
    """

    def __init__(self, message: str = "", details: dict | None = None) -> None:
        """
        参数:
            message: 错误描述信息
            details: 附加详情（可选，用于记录更多上下文）
        """
        super().__init__(message)
        self.message = message
        self.details = details or {}


# =============================================================================
# 配置相关异常
# =============================================================================

class ConfigError(WhiteSalaryError):
    """配置文件有问题时抛出（比如配置项缺失、格式错误）。"""


class ConfigFileNotFoundError(ConfigError):
    """配置文件找不到时抛出。"""


class ConfigValidationError(ConfigError):
    """配置项校验失败时抛出（比如端口号不是数字）。"""


# =============================================================================
# LLM相关异常
# =============================================================================

class LLMError(WhiteSalaryError):
    """LLM（大语言模型）相关异常的基类。"""


class LLMConnectionError(LLMError):
    """连接LLM服务失败时抛出（比如网络不通、API地址错误）。"""


class LLMAuthenticationError(LLMError):
    """LLM认证失败时抛出（比如API密钥错误或过期）。"""


class LLMRateLimitError(LLMError):
    """LLM API调用太频繁被限流时抛出。"""


class LLMResponseError(LLMError):
    """LLM返回了异常响应时抛出（比如返回空内容、格式错误）。"""


# =============================================================================
# ASR（语音识别）相关异常
# =============================================================================

class ASRError(WhiteSalaryError):
    """语音识别相关异常的基类。"""


class ASRModelNotLoadedError(ASRError):
    """ASR模型还没加载完就尝试识别时抛出。"""


class ASRTranscriptionError(ASRError):
    """语音识别过程中出错时抛出。"""


# =============================================================================
# TTS（语音合成）相关异常
# =============================================================================

class TTSError(WhiteSalaryError):
    """语音合成相关异常的基类。"""


class TTSModelNotLoadedError(TTSError):
    """TTS模型还没加载完就尝试合成时抛出。"""


class TTSSynthesisError(TTSError):
    """语音合成过程中出错时抛出。"""


# =============================================================================
# 视觉相关异常
# =============================================================================

class VisionError(WhiteSalaryError):
    """计算机视觉相关异常的基类。"""


# =============================================================================
# 唱歌相关异常
# =============================================================================

class SingingError(WhiteSalaryError):
    """唱歌模块相关异常的基类。"""


# =============================================================================
# 虚拟形象相关异常
# =============================================================================

class AvatarError(WhiteSalaryError):
    """虚拟形象相关异常的基类。"""


# =============================================================================
# 存储相关异常
# =============================================================================

class StorageError(WhiteSalaryError):
    """数据存储相关异常的基类。"""


class StorageReadError(StorageError):
    """读取数据失败时抛出。"""


class StorageWriteError(StorageError):
    """写入数据失败时抛出。"""


# =============================================================================
# 会话相关异常
# =============================================================================

class SessionError(WhiteSalaryError):
    """会话管理相关异常的基类。"""


class SessionNotFoundError(SessionError):
    """找不到指定的会话时抛出。"""


# =============================================================================
# 管道相关异常
# =============================================================================

class PipelineError(WhiteSalaryError):
    """处理管道相关异常的基类。"""
